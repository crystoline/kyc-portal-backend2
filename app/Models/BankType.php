class BankType extends Model
{
    //use SoftDeletes;

    public $table = 'bank_types';
    
   /* const CREATED_AT = 'created_at';
    const UPDATED_AT = 'updated_at';*/


    protected $dates = ['deleted_at'];



    public $fillable = [
        'name',
        'status'
    ];

    /**
     * The attributes that should be casted to native types.
     *
     * @var array
     */
    protected $casts = [
        'id' => 'integer',
        'name' => 'string',
        'status' => 'integer'
    ];

    /**
     * Validation rules
     *
     * @var array
     */
    public static $rules = [
        'name' => 'required',
        'status' => 'required'
    ];

    /**
     * @return HasMany
     **/
    public function banks(): HasMany
    {
        return $this->hasMany(Bank::class, 'bank_type_id');
    }
